class Ex3{
    public static void main(String[] args){

      // 자료형 변수나 상수 사용법
      // 자료형 변수명(상수명) = 데이터 ;
      // 논리형 ; boolean , 데이터에는 true, false 둘중 하나 사용
     //           조건식에 사용된다.
 
         boolean result ;  // result라는 변수를 boolean형으로 선언
                           // result에는 true, false만 저장된다.  

      // result는 true와 같다라는 뜻이 아니다.    
      // true라는 데이트를 result에 대입(저장)하라는 뜻이다.
      //   = 기준으로 왼쪽과 오른쪽의 자료형은 같아야 한다.

        result = true ;
        result = false;
	result = 'A' ;
      // 변수를 호출하면 변수가 가지고 있는 데이터가 나옴 
        System.out.println(result);
       
   }
}




